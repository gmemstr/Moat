  <div id="faq">
    <h2>WTF is Moat?</h2>
    <p>Moat is a project by Gabriel Simmer to help bring 
      <a href="http://voat.co">Voat</a> to a nicer mobile
      interface.</p>
      <br>

      <h2>Why does it take so long to load sometimes?</h2>
      <p>It's probably because the Voat cache has expired. Every
        30 minutes it is refreshed, and during this time the site
        can take a little while longer to load. Note that the caching
        is done by Moat, it is not something Voat does :p<br>
        Note that due to some recent changes to the Moat backend, I can
        no-longer properly cache things for the moment due to disk space
        restrictions.</p>

        <br>

        <h2>What is it built with?</h2>
        <p>Wizardy. But seriously, the site is running on
          XAMPP for Windows, and using PHP to get and generate
          the actual content from Voat. A plentiful of CSS is
          used to make the site look nicer and scale properly.</p>
